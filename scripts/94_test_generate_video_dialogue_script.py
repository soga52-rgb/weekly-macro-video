def build_content_parts(summary: Dict[str, Any], week_dir: Path, use_image_input: bool) -> List[Dict[str, Any]]:
    payload = build_scene_payload(summary, week_dir, use_image_input)
    prompt = USER_PROMPT_TEMPLATE.replace(
        "{scene_payload_json}",
        json.dumps(payload, ensure_ascii=False, indent=2),
    )

    parts: List[Dict[str, Any]] = [{"text": prompt}]

    if not use_image_input:
        return parts

    for scene in as_list(summary.get("video_visual_scenes")):
        if not isinstance(scene, dict):
            continue
        scene_id = str(scene.get("scene_id", ""))
        image_path = find_scene_image(week_dir, scene_id)
        if not image_path:
            continue
        parts.append({
            "text": f"以下圖片對應 {scene_id}。請用它理解該幕畫面元素；若圖片文字與 weekly_forest_summary 不一致，以 weekly_forest_summary 為準。"
        })
        parts.append(encode_image_part(image_path))

    return parts


def extract_json(text: str) -> Dict[str, Any]:
    text = text.strip()
    start = text.find("{")
    end = text.rfind("}")
    if start == -1 or end == -1 or end <= start:
        raise ValueError("Gemini response does not contain a JSON object.")
    return json.loads(text[start:end + 1], strict=False)


def call_gemini_json(parts: List[Dict[str, Any]], model: str, api_key: str, temperature: float) -> Dict[str, Any]:
    endpoint = (
        "https://generativelanguage.googleapis.com/v1beta/models/"
        + urllib.parse.quote(model)
        + ":generateContent?key="
        + urllib.parse.quote(api_key)
    )
    payload = {
        "systemInstruction": {"parts": [{"text": SYSTEM_PROMPT}]},
        "contents": [{"role": "user", "parts": parts}],
        "generationConfig": {
            "temperature": temperature,
            "topP": 0.9,
            "responseMimeType": "application/json",
        },
    }
    request = urllib.request.Request(
        endpoint,
        data=json.dumps(payload).encode("utf-8"),
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(request, timeout=360) as response:
            raw = response.read().decode("utf-8")
    except urllib.error.HTTPError as exc:
        detail = exc.read().decode("utf-8", errors="replace")
        raise RuntimeError(f"Gemini dialogue HTTPError {exc.code}: {detail}") from exc
    except (urllib.error.URLError, TimeoutError, socket.timeout) as exc:
        raise RuntimeError(f"Gemini dialogue request failed or timed out: {exc}") from exc

    result = json.loads(raw)
    candidates = result.get("candidates") or []
    if not candidates:
        raise RuntimeError(f"Gemini returned no candidates. Preview: {raw[:1200]}")
    parts_out = (candidates[0].get("content") or {}).get("parts") or []
    text = "".join(str(part.get("text", "")) for part in parts_out)
    if not text.strip():
        raise RuntimeError(f"Gemini returned empty text. Preview: {raw[:1200]}")
    return extract_json(text)


def estimate_seconds(text: str) -> int:
    return max(3, min(50, round(len(text or "") / 4.7)))


def short_subtitle(text: str, max_len: int = 25) -> str:
    text = (text or "").strip().replace("\n", " ")
    if len(text) <= max_len:
        return text
    return text[:max_len].rstrip("，。；、 ") + "…"






def sanitize_tone_phrases(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Post-process only hard-banned tone phrases that damage institutional program quality.
    Do not use this function to rewrite ordinary spoken style; general tone is controlled by SYSTEM_PROMPT.
    """
    replacements = {
        "精神分裂的一週": "結構性拉鋸的一週",
        "精神分裂": "結構性拉鋸",
        "核彈級的利空": "非常沉重的利空",
        "核彈級利空": "非常沉重的利空",
        "核彈級": "高權重",
        "躺平任人捶打": "承受明顯貶值壓力",
        "躺平承受重壓": "承受明顯貶值壓力",
        "躺平": "承受壓力",
        "崩盤": "大幅修正",
        "全面失守": "壓力明顯升高",
        "哀鴻遍野": "壓力明顯升高",
        "免死金牌": "政策安全墊",
        "尚方寶劍": "政策工具",
    }

    hard_avoid_terms = [
        "精神分裂",
        "核彈級",
        "躺平任人捶打",
        "崩盤",
        "全面失守",
        "哀鴻遍野",
        "免死金牌",
        "尚方寶劍",
    ]

    changed_turns: List[str] = []

    for scene in data.get("scene_dialogues", []) or []:
        if not isinstance(scene, dict):
            continue

        for field in ["scene_summary", "single_message"]:
            if isinstance(scene.get(field), str):
                original = scene[field]
                updated = original
                for old, new in replacements.items():
                    updated = updated.replace(old, new)
                if updated != original:
                    scene[field] = updated

        avoid = scene.get("avoid_saying")
        if isinstance(avoid, list):
            for term in hard_avoid_terms:
                if term not in avoid:
                    avoid.append(term)
            scene["avoid_saying"] = avoid

        for turn in scene.get("speaker_turns", []) or []:
            if not isinstance(turn, dict):
                continue

            changed = False
            for field in ["spoken_text", "subtitle_text", "visual_reference"]:
                if not isinstance(turn.get(field), str):
                    continue
                original = turn[field]
                updated = original
                for old, new in replacements.items():
                    updated = updated.replace(old, new)
                if updated != original:
                    turn[field] = updated
                    changed = True

            if changed:
                changed_turns.append(str(turn.get("turn_id", "")))

    if changed_turns:
        qc = data.setdefault("quality_checks", {})
        prev = str(qc.get("risk_control_note", ""))
        qc["risk_control_note"] = (
            (prev + " " if prev else "")
            + f"Sanitized hard-banned tone phrases in turns: {changed_turns}."
        )

    return data


def ensure_minimum_tom_turns(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Add a quality note when Tom turns are too short.
    We do not rewrite generated content here, but we surface the issue clearly.
    """
    short_turns: List[str] = []
    for scene in data.get("scene_dialogues", []) or []:
        if not isinstance(scene, dict):
            continue
        scene_id = str(scene.get("scene_id", ""))
        for turn in scene.get("speaker_turns", []) or []:
            if not isinstance(turn, dict):
                continue
            if turn.get("speaker") != "Tom":
                continue
            spoken = str(turn.get("spoken_text", "")).strip()
            # Allow final closing turn and very short connector questions, but flag very short ones.
            if 0 < len(spoken) < 18:
                short_turns.append(str(turn.get("turn_id") or scene_id))

    if short_turns:
        qc = data.setdefault("quality_checks", {})
        prev = str(qc.get("dialogue_naturalness_note", ""))
        qc["dialogue_naturalness_note"] = (
            (prev + " " if prev else "")
            + f"Tom has very short turns that may create visual flicker in TTS/video: {short_turns}."
        )
    return data


def ensure_final_tom_closing(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Ensure the last scene ends with Tom, so the video has a proper closing rhythm.
    """
    scenes = data.get("scene_dialogues")
    if not isinstance(scenes, list) or not scenes:
        return data

    last_scene = scenes[-1]
    if not isinstance(last_scene, dict):
        return data

    turns = last_scene.setdefault("speaker_turns", [])
    if not isinstance(turns, list):
        turns = []
        last_scene["speaker_turns"] = turns

    if turns and isinstance(turns[-1], dict) and turns[-1].get("speaker") == "Tom":
        return data

    scene_id = str(last_scene.get("scene_id", "scene"))
    turn_idx = len(turns) + 1
    closing_text = (
        "明白了。在這些關鍵訊號明朗之前，市場仍需要保持彈性、持續觀察風險變化。"
        "非常感謝 Miranda 的深度拆解，也謝謝觀眾朋友收看，我們下週見。"
    )

    turns.append({
        "turn_id": f"{scene_id}_turn_{turn_idx:02d}",
        "speaker": "Tom",
        "display_name": "Tom",
        "role_label": "主持人",
        "spoken_text": closing_text,
        "subtitle_text": "感謝收看，保持彈性，我們下週見。",
        "speaker_label_text": "Tom｜主持人",
        "estimated_seconds": estimate_seconds(closing_text),
        "news_reference": [],
        "visual_reference": last_scene.get("scene_id", ""),
    })

    qc = data.setdefault("quality_checks", {})
    prev = str(qc.get("continuity_note", ""))
    qc["continuity_note"] = (prev + " " if prev else "") + "Added final Tom closing turn for proper ending rhythm."
    return data


def enrich_cross_scene_news_references(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Patch common cross-scene causal references so downstream video/news labels do not miss them.
    This is intentionally conservative and only adds obvious references based on spoken_text.
    """
    keyword_refs = [
        (["房市", "成長隱憂"], "房市數據疲軟引發成長降溫擔憂"),
        (["經濟成長隱憂"], "房市數據疲軟引發成長降溫擔憂"),
        (["避險", "黃金"], "經濟成長隱憂支撐黃金避險需求"),
        (["升息", "會議紀錄"], "Fed會議紀錄重啟升息討論"),
        (["美伊", "和平"], "美伊和平協議傳聞"),
    ]

    for scene in data.get("scene_dialogues", []) or []:
        if not isinstance(scene, dict):
            continue
        for turn in scene.get("speaker_turns", []) or []:
            if not isinstance(turn, dict):
                continue
            spoken = str(turn.get("spoken_text", ""))
            refs = turn.get("news_reference")
            if not isinstance(refs, list):
                refs = []
            for keywords, ref in keyword_refs:
                if all(k in spoken for k in keywords) and ref not in refs:
                    refs.append(ref)
            turn["news_reference"] = refs

    return data



def split_sentences_for_turn(text: str) -> List[str]:
    """Split Chinese spoken text into sentence-like chunks while keeping punctuation."""
    cleaned = (text or "").strip()
    if not cleaned:
        return []
    parts = re.split(r"(?<=[。！？?!])", cleaned)
    return [p.strip() for p in parts if p and p.strip()]


def bridge_text_for_scene(scene_type: str, scene_title: str) -> str:
    """Create a short, stable Tom bridge when a Miranda monologue needs to be split."""
    scene_type = str(scene_type or "")
    if scene_type == "inflation_expectation":
        return "所以關鍵不是油價跌了就代表通膨問題結束，而是要分清楚能源端和核心通膨的訊號，對嗎？"
    if scene_type == "rate_expectation":
        return "也就是說，債市真正害怕的不是單一數據，而是政策路徑重新被定價，這點很關鍵。"
    if scene_type == "dollar_index":
        return "所以美元不是沒有支撐，而是上方同時被成長疑慮壓住，這才形成高檔震盪。"
    if scene_type == "asia_fx_gold":
        return "這樣看起來，亞幣和黃金其實不是同一套定價邏輯，分化反而是合理的結果。"
    if scene_type == "next_week_roadmap":
        return "所以下週其實不是猜單一方向，而是要驗證這兩條線索哪一條會被市場確認。"
    return "所以這裡的重點，是把畫面上的市場現象拆成不同驅動因子來看，不能只看單一數字。"


def rebalance_long_speaker_turns(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Prevent 2-turn scenes with very long Miranda monologues.
    If a scene has too few turns and a Miranda turn is too long, split it into:
    Miranda first half -> Tom bridge -> Miranda second half.
    """
    changed_scenes: List[str] = []

    for scene in data.get("scene_dialogues", []) or []:
        if not isinstance(scene, dict):
            continue

        turns = [t for t in as_list(scene.get("speaker_turns")) if isinstance(t, dict)]
        if len(turns) >= 3:
            continue

        new_turns: List[Dict[str, Any]] = []
        changed = False
        scene_id = str(scene.get("scene_id", "scene"))
        scene_type = str(scene.get("scene_type", ""))
        scene_title = str(scene.get("screen_title", ""))

        for turn in turns:
            speaker = str(turn.get("speaker", ""))
            spoken = str(turn.get("spoken_text", "")).strip()
            estimated = int(turn.get("estimated_seconds") or estimate_seconds(spoken))

            if speaker == "Miranda" and estimated > 35:
                sentences = split_sentences_for_turn(spoken)
                if len(sentences) >= 4:
                    midpoint = max(2, len(sentences) // 2)
                    first_text = "".join(sentences[:midpoint]).strip()
                    second_text = "".join(sentences[midpoint:]).strip()

                    first = dict(turn)
                    first["spoken_text"] = first_text
                    first["subtitle_text"] = short_subtitle(str(first.get("subtitle_text") or first_text))
                    first["estimated_seconds"] = estimate_seconds(first_text)

                    bridge_text = bridge_text_for_scene(scene_type, scene_title)
                    bridge = {
                        "turn_id": "",
                        "speaker": "Tom",
                        "display_name": "Tom",
                        "role_label": "主持人",
                        "spoken_text": bridge_text,
                        "subtitle_text": short_subtitle(bridge_text),
                        "speaker_label_text": "Tom｜主持人",
                        "estimated_seconds": max(8, estimate_seconds(bridge_text)),
                        "news_reference": [],
                        "visual_reference": turn.get("visual_reference", scene_id),
                    }

                    second = dict(turn)
                    second["spoken_text"] = second_text
                    second["subtitle_text"] = short_subtitle(second_text)
                    second["estimated_seconds"] = estimate_seconds(second_text)

                    new_turns.extend([first, bridge, second])
                    changed = True
                    continue

            new_turns.append(turn)

        if changed:
            for idx, turn in enumerate(new_turns, start=1):
                speaker = str(turn.get("speaker") or "Miranda")
                role = "主持人" if speaker == "Tom" else "總經策略師"
                spoken = str(turn.get("spoken_text", "")).strip()
                turn["turn_id"] = f"{scene_id}_turn_{idx:02d}"
                turn["speaker"] = speaker
                turn["display_name"] = speaker
                turn["role_label"] = role
                turn["speaker_label_text"] = f"{speaker}｜{role}"
                turn["subtitle_text"] = short_subtitle(str(turn.get("subtitle_text") or spoken))
                turn["estimated_seconds"] = int(turn.get("estimated_seconds") or estimate_seconds(spoken))
                turn.setdefault("news_reference", [])
                turn.setdefault("visual_reference", scene_id)

            scene["speaker_turns"] = new_turns
            changed_scenes.append(scene_id)

    if changed_scenes:
        qc = data.setdefault("quality_checks", {})
        prev = str(qc.get("dialogue_naturalness_note", ""))
        qc["dialogue_naturalness_note"] = (
            (prev + " " if prev else "")
            + f"Rebalanced long Miranda turns in scenes: {changed_scenes}."
        )

    return data


def normalize_dialogue(data: Dict[str, Any], summary: Dict[str, Any], week_dir: Path, used_image_input: bool) -> Dict[str, Any]:
    scene_output_control = get_scene_output_control(summary, week_dir, used_image_input)
    control_ids = [item.get("scene_id", "") for item in scene_output_control if item.get("scene_id")]

    data.setdefault("meta", {})
    data["meta"].setdefault("source", "scene_images + scene_control_list + scene_dialogue_context + supporting_context")
    data["meta"].setdefault("purpose", "image-grounded Tom / Miranda formal dialogue script before TTS")
    data["meta"].setdefault("week_range", (summary.get("meta") or {}).get("week_range", ""))
    data["meta"]["scene_count"] = len(control_ids)
    data["meta"]["used_image_input"] = bool(used_image_input)

    data.setdefault("speaker_profiles", {
        "Tom": {"display_name": "Tom", "role_label": "主持人", "voice_role": "male_host", "screen_position": "lower_left"},
        "Miranda": {"display_name": "Miranda", "role_label": "總經策略師", "voice_role": "female_strategist", "screen_position": "lower_left"},
    })

    existing = data.get("scene_dialogues")
    if not isinstance(existing, list):
        existing = []
    by_id = {
        str(scene.get("scene_id", "")): scene
        for scene in existing
        if isinstance(scene, dict)
    }

    aligned_scenes: List[Dict[str, Any]] = []
    missing: List[str] = []

    for ctrl in scene_output_control:
        scene_id = str(ctrl.get("scene_id", ""))
        scene = dict(by_id.get(scene_id, {}))
        if not scene:
            missing.append(scene_id)
            scene = {
                "scene_id": scene_id,
                "scene_type": ctrl.get("scene_type", ""),
                "screen_title": ctrl.get("screen_title", ""),
                "single_message": ctrl.get("single_message", ""),
                "visual_anchor": {
                    "visible_elements_used": [],
                    "must_align_with": [],
                    "do_not_expand": [],
                },
                "source_material_used": [],
                "speaker_turns": [],
                "scene_summary": "",
                "avoid_saying": [],
            }

        scene["scene_id"] = scene_id
        scene["scene_type"] = scene.get("scene_type") or ctrl.get("scene_type", "")
        scene["screen_title"] = scene.get("screen_title") or ctrl.get("screen_title", "")
        scene["single_message"] = scene.get("single_message") or ctrl.get("single_message", "")

        turns = scene.get("speaker_turns") or []
        if not isinstance(turns, list):
            turns = []

        normalized_turns = []
        for idx, turn in enumerate(turns, start=1):
            if not isinstance(turn, dict):
                continue
            speaker = str(turn.get("speaker") or "Miranda").strip()
            if speaker not in {"Tom", "Miranda"}:
                speaker = "Miranda"
            role = "主持人" if speaker == "Tom" else "總經策略師"
            spoken = str(turn.get("spoken_text", "")).strip()
            turn["speaker"] = speaker
            turn["display_name"] = speaker
            turn["role_label"] = role
            turn["speaker_label_text"] = f"{speaker}｜{role}"
            turn["turn_id"] = turn.get("turn_id") or f"{scene_id}_turn_{idx:02d}"
            turn["subtitle_text"] = short_subtitle(str(turn.get("subtitle_text") or spoken))
            estimated = int(turn.get("estimated_seconds") or estimate_seconds(spoken))
            if speaker == "Tom" and len(spoken) >= 18:
                estimated = max(8, estimated)
            turn["estimated_seconds"] = estimated
            turn.setdefault("news_reference", [])
            turn.setdefault("visual_reference", scene_id)
            normalized_turns.append(turn)

        scene["speaker_turns"] = normalized_turns
        scene.setdefault("visual_anchor", {
            "visible_elements_used": [],
            "must_align_with": [],
            "do_not_expand": [],
        })
        scene.setdefault("source_material_used", [])
        scene.setdefault("avoid_saying", [])
        aligned_scenes.append(scene)

    data["scene_dialogues"] = aligned_scenes

    data.setdefault("tts_plan", {
        "voice_strategy": "Tom male host, Miranda female strategist",
        "needs_tts": True,
        "needs_speaker_label_overlay": True,
        "subtitle_mode": "turn_by_turn",
    })

    qc = data.setdefault("quality_checks", {})
    qc.setdefault("scene_alignment_note", "")
    qc["scene_alignment_note"] = (
        qc["scene_alignment_note"] + " " if qc["scene_alignment_note"] else ""
    ) + f"Aligned scene_dialogues to control ids: {control_ids}."
    if missing:
        qc["scene_alignment_note"] += f" Missing scenes were backfilled as placeholders: {missing}."

    return data


def build_markdown(data: Dict[str, Any]) -> str:
    lines: List[str] = []
    lines.append(f"# 影片雙人正式旁白稿｜{data.get('title', '')}")
    lines.append("")

    if data.get("opening_hook"):
        lines.append("## Opening Hook")
        lines.append("")
        lines.append(str(data.get("opening_hook", "")))
        lines.append("")

    for scene in data.get("scene_dialogues", []) or []:
        lines.append(f"## {scene.get('scene_id', '')}｜{scene.get('screen_title', '')}")
        lines.append("")

        if scene.get("single_message"):
            lines.append(f"畫面訊息：{scene.get('single_message')}")
            lines.append("")

        if scene.get("visual_anchor"):
            lines.append("### Visual Anchor")
            lines.append("")
            for key, value in (scene.get("visual_anchor") or {}).items():
                lines.append(f"- {key}: {value}")
            lines.append("")

        turns = scene.get("speaker_turns") or []
        for turn in turns:
            speaker = turn.get("speaker", "")
            role = turn.get("role_label", "")
            spoken = turn.get("spoken_text", "")
            subtitle = turn.get("subtitle_text", "")

            lines.append(f"**[{speaker}｜{role}]** {spoken}")
            if subtitle:
                lines.append(f"> 字幕：{subtitle}")
            if turn.get("visual_reference"):
                lines.append(f"> 畫面：{turn.get('visual_reference')}")
            if turn.get("news_reference"):
                lines.append(f"> 素材：{turn.get('news_reference')}")
            lines.append("")

        if scene.get("scene_summary"):
            lines.append(f"小結：{scene.get('scene_summary')}")
            lines.append("")

    return "\n".join(lines).strip() + "\n"


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--week-dir", type=str, default="")
    parser.add_argument("--use-image-input", type=str, default="")
    args = parser.parse_args()

    api_key = os.getenv("GEMINI_API_KEY", "").strip()
    if not api_key:
        raise EnvironmentError("Missing GEMINI_API_KEY.")

    model = os.getenv("GEMINI_DIALOGUE_MODEL", DEFAULT_DIALOGUE_MODEL).strip() or DEFAULT_DIALOGUE_MODEL
    try:
        temperature = float(os.getenv("DIALOGUE_TEMPERATURE", "0.55").strip())
    except ValueError:
        temperature = 0.55

    use_image_input = parse_bool(
        args.use_image_input or os.getenv("USE_IMAGE_INPUT_FOR_DIALOGUE", "true"),
        default=True,
    )

    week_dir = resolve_week_dir(args.week_dir)
    summary_path = week_dir / "weekly_forest_summary.json"
    summary = load_json(summary_path, {})
    if not summary:
        raise FileNotFoundError(f"Missing or empty weekly_forest_summary.json: {summary_path}")

    scene_count = len(as_list(summary.get("video_visual_scenes")))
    found_images = sum(
        1
        for scene in as_list(summary.get("video_visual_scenes"))
        if isinstance(scene, dict) and find_scene_image(week_dir, str(scene.get("scene_id", "")))
    )
    effective_image_input = bool(use_image_input and found_images > 0)

    print(f"[INFO] Week dir: {week_dir}")
    print(f"[INFO] Dialogue model: {model}")
    print(f"[INFO] Dialogue temperature: {temperature}")
    print(f"[INFO] USE_IMAGE_INPUT_FOR_DIALOGUE requested: {use_image_input}")
    print(f"[INFO] Scene images found: {found_images}/{scene_count}")
    print(f"[INFO] Effective image input: {effective_image_input}")

    content_parts = build_content_parts(summary, week_dir, effective_image_input)
    data = call_gemini_json(
        parts=content_parts,
        model=model,
        api_key=api_key,
        temperature=temperature,
    )
    # === Core post-processing pipeline ===
    # 1. Rebalance long monologues before adding final closing, so the last scene can also be split if needed.
    data = rebalance_long_speaker_turns(data)

    # 2. Ensure the final scene ends with Tom after possible turn rebalancing.
    data = ensure_final_tom_closing(data)

    # 3. Normalize scene alignment, speaker labels, turn IDs, subtitles, and estimated seconds.
    data = normalize_dialogue(data, summary, week_dir, effective_image_input)

    # 4. Add cross-scene news references after all turns are finalized.
    data = enrich_cross_scene_news_references(data)

    # 5. Apply the existing hard-tone sanitizer once only, after the final structure is stable.
    data = sanitize_tone_phrases(data)

    # 6. Final QC note for very short Tom turns.
    data = ensure_minimum_tom_turns(data)

    json_path = week_dir / "video_dialogue_script.json"
    md_path = week_dir / "video_dialogue_script.md"
    save_json(json_path, data)
    save_text(md_path, build_markdown(data))

    print(f"[OK] Created {json_path}")
    print(f"[OK] Created {md_path}")


if __name__ == "__main__":
    main()
