def parse_bool(value: str, default: bool = True) -> bool:
    if value is None:
        return default
    text = str(value).strip().lower()
    if text in {"1", "true", "yes", "y", "on"}:
        return True
    if text in {"0", "false", "no", "n", "off"}:
        return False
    return default


def as_list(value: Any) -> List[Any]:
    if isinstance(value, list):
        return value
    if value in (None, ""):
        return []
    return [value]


def find_latest_week_dir() -> Path:
    week_dirs = [p for p in OUTPUT_WEEKLY_DIR.iterdir() if p.is_dir()]
    if not week_dirs:
        raise FileNotFoundError("No weekly output folder found under output/weekly/")
    week_dirs.sort(key=lambda p: p.name, reverse=True)
    return week_dirs[0]


def resolve_week_dir(week_dir_arg: str) -> Path:
    if week_dir_arg:
        week_dir = Path(week_dir_arg)
        if week_dir.exists():
            return week_dir
        candidate = OUTPUT_WEEKLY_DIR / week_dir_arg
        if candidate.exists():
            return candidate
        raise FileNotFoundError(f"Week folder not found: {week_dir_arg}")
    return find_latest_week_dir()


def load_json(path: Path, default: Any = None) -> Any:
    if not path.exists():
        return default
    with path.open("r", encoding="utf-8-sig") as f:
        return json.load(f)


def save_json(path: Path, data: Dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


def save_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def find_scene_image(week_dir: Path, scene_id: str) -> Optional[Path]:
    candidates = []
    image_dir = week_dir / "visual_sequence_images"
    for ext in [".png", ".jpg", ".jpeg", ".webp"]:
        candidates.append(image_dir / f"{scene_id}{ext}")
        candidates.append(week_dir / f"{scene_id}{ext}")

    if image_dir.exists():
        for p in image_dir.iterdir():
            if p.is_file() and p.suffix.lower() in {".png", ".jpg", ".jpeg", ".webp"}:
                if p.stem.lower() == scene_id.lower():
                    candidates.append(p)

    for path in candidates:
        if path.exists() and path.is_file():
            return path
    return None


def encode_image_part(path: Path) -> Dict[str, Any]:
    mime_type, _ = mimetypes.guess_type(str(path))
    if not mime_type:
        mime_type = "image/png"
    data = base64.b64encode(path.read_bytes()).decode("ascii")
    return {
        "inline_data": {
            "mime_type": mime_type,
            "data": data,
        }
    }


def get_scene_output_control(summary: Dict[str, Any], week_dir: Path, use_image_input: bool) -> List[Dict[str, Any]]:
    """
    Output control priority:
    1. actual scene images if image input is effective
    2. scene_control_list from Step 91
    3. video_visual_scenes from Step 91
    """
    visual_by_id = {
        str(item.get("scene_id", "")): item
        for item in as_list(summary.get("video_visual_scenes"))
        if isinstance(item, dict)
    }
    control_by_id = {
        str(item.get("scene_id", "")): item
        for item in as_list(summary.get("scene_control_list"))
        if isinstance(item, dict)
    }

    if use_image_input:
        image_dir = week_dir / "visual_sequence_images"
        image_scene_ids: List[str] = []
        if image_dir.exists():
            image_files = sorted(
                p for p in image_dir.iterdir()
                if p.is_file() and p.suffix.lower() in {".png", ".jpg", ".jpeg", ".webp"}
            )
            image_scene_ids = [p.stem for p in image_files if p.stem.startswith("scene_")]

        if image_scene_ids:
            output = []
            for idx, scene_id in enumerate(image_scene_ids, start=1):
                v = visual_by_id.get(scene_id, {})
                c = control_by_id.get(scene_id, {})
                output.append({
                    "scene_id": scene_id,
                    "scene_type": v.get("scene_type") or c.get("scene_type", ""),
                    "screen_title": v.get("screen_title") or c.get("screen_title", ""),
                    "single_message": v.get("single_message") or c.get("single_message", ""),
                    "image_file": f"visual_sequence_images/{scene_id}.png",
                    "source": "actual_image_files",
                })
            return output

    control = as_list(summary.get("scene_control_list"))
    if control:
        output = []
        for item in control:
            if not isinstance(item, dict):
                continue
            scene_id = str(item.get("scene_id", ""))
            v = visual_by_id.get(scene_id, {})
            output.append({
                "scene_id": scene_id,
                "scene_type": v.get("scene_type") or item.get("scene_type", ""),
                "screen_title": v.get("screen_title") or item.get("screen_title", ""),
                "single_message": v.get("single_message") or item.get("single_message", ""),
                "image_file": "",
                "source": "scene_control_list",
            })
        return output

    scenes = as_list(summary.get("video_visual_scenes"))
    return [
        {
            "scene_id": str(scene.get("scene_id", f"scene_{idx:02d}")),
            "scene_type": scene.get("scene_type", ""),
            "screen_title": scene.get("screen_title", ""),
            "single_message": scene.get("single_message", ""),
            "image_file": "",
            "source": "video_visual_scenes",
        }
        for idx, scene in enumerate(scenes, start=1)
        if isinstance(scene, dict)
    ]


def build_compact_scenes(summary: Dict[str, Any], week_dir: Path, use_image_input: bool) -> List[Dict[str, Any]]:
    scenes = as_list(summary.get("video_visual_scenes"))
    dialogue_context = {
        str(item.get("scene_id", "")): item
        for item in as_list(summary.get("scene_dialogue_context"))
        if isinstance(item, dict)
    }
    narration = {
        str(item.get("scene_id", "")): item
        for item in as_list(summary.get("narration_outline"))
        if isinstance(item, dict)
    }

    compact: List[Dict[str, Any]] = []
    for scene in scenes:
        if not isinstance(scene, dict):
            continue
        scene_id = str(scene.get("scene_id", ""))
        image_path = find_scene_image(week_dir, scene_id)
        compact.append({
            "scene_id": scene_id,
            "scene_type": scene.get("scene_type", ""),
            "screen_title": scene.get("screen_title", ""),
            "single_message": scene.get("single_message", ""),
            "on_screen_labels": scene.get("on_screen_labels", []),
            "must_show_numbers": scene.get("must_show_numbers", []),
            "visual_metaphor": scene.get("visual_metaphor", ""),
            "image_file": str(image_path.relative_to(week_dir)) if image_path else "",
            "image_available": bool(image_path and use_image_input),
            "scene_dialogue_context": dialogue_context.get(scene_id, {}),
            "narration_outline": narration.get(scene_id, {}),
        })
    return compact


